PACKAGE TESTPACKAGE
AS
  PROCEDURE TEST_TRANS_CUST_DETAILS (
		p_customer_id		 	IN 			VARCHAR2,
	    cur_OUT 				OUT 		PKGENTLIB_ARCHITECTURE.CURENTLIB_ARCHITECTURE
	);
END TESTPACKAGE;
  ----------PKG BODY:------------------
/
CREATE OR REPLACE
PACKAGE BODY TESTPACKAGE
AS
	PROCEDURE TEST_TRANS_CUST_DETAILS
	(
		p_customer_id IN VARCHAR2,
	    cur_OUT OUT PKGENTLIB_ARCHITECTURE.CURENTLIB_ARCHITECTURE
	)
	AS
	BEGIN
	    OPEN cur_OUT FOR
		SELECT * 
		FROM Customers
		WHERE CustomerID = p_customer_id;

	END TEST_TRANS_CUST_DETAILS;
END TESTPACKAGE;
