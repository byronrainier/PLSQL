PACKAGE PKGENTLIB_ARCHITECTURE
AS
type CURENTLIB_ARCHITECTURE
IS
  REF
  CURSOR;
END PKGENTLIB_ARCHITECTURE;
  ----------PKG BODY:------------------
/
CREATE OR REPLACE
PACKAGE BODY PKGENTLIB_ARCHITECTURE
AS
BEGIN
  DBMS_OUTPUT.PUT_LINE('OUTPUT');
END PKGENTLIB_ARCHITECTURE;
